static const struct kat rfc_kats[] = {
	{ "1", rfc_0_seed, rfc_0_pub, NULL, 0, rfc_0_sig },
	{ "2", rfc_1_seed, rfc_1_pub, rfc_1_msg, 1, rfc_1_sig },
	{ "3", rfc_2_seed, rfc_2_pub, rfc_2_msg, 2, rfc_2_sig },
	{ "1024", rfc_3_seed, rfc_3_pub, rfc_3_msg, 1023, rfc_3_sig },
	{ "SHA(abc)", rfc_4_seed, rfc_4_pub, rfc_4_msg, 64, rfc_4_sig },
};

#define NKATS (sizeof(rfc_kats) / sizeof(rfc_kats[0]))

static int failures;

#define CHECK(cond, ...) do { \
	if (!(cond)) { \
		failures++; \
		printf("FAIL: " __VA_ARGS__); \
		printf("\n"); \
	} \
} while (0)

/* Group order L (little-endian), for the non-canonical-s negative. */
static const u8 ed_L[32] = {
	0xed, 0xd3, 0xf5, 0x5c, 0x1a, 0x63, 0x12, 0x58,
	0xd6, 0x9c, 0xf7, 0xa2, 0xde, 0xf9, 0xde, 0x14,
	0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
	0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x10
};

static void run_rfc_kats(void)
{
	unsigned int i;

	for (i = 0; i < NKATS; i++) {
		const struct kat *k = &rfc_kats[i];
		struct sfs_ed25519_key key;
		u8 sig[64];
		u8 tmp[64];

		CHECK(sfs_ed25519_expand(sha512_cb, NULL, k->seed, &key) == 0,
		      "RFC %s: expand", k->name);
		CHECK(memcmp(key.pub, k->pub, 32) == 0,
		      "RFC %s: derived pubkey != vector pubkey", k->name);
		CHECK(sfs_ed25519_sign(sha512_cb, NULL, &key, k->msg,
				       k->msg_len, sig) == 0,
		      "RFC %s: sign", k->name);
		CHECK(memcmp(sig, k->sig, 64) == 0,
		      "RFC %s: deterministic signature != vector", k->name);
		CHECK(sfs_ed25519_verify(sha512_cb, NULL, k->pub, k->msg,
					 k->msg_len, k->sig) == 1,
		      "RFC %s: verify(vector) rejected", k->name);

		/* ── negatives ────────────────────────────────────────── */
		memcpy(tmp, k->sig, 64);
		tmp[0] ^= 0x01;                       /* R tampered */
		CHECK(sfs_ed25519_verify(sha512_cb, NULL, k->pub, k->msg,
					 k->msg_len, tmp) == 0,
		      "RFC %s: flipped R byte accepted", k->name);
		memcpy(tmp, k->sig, 64);
		tmp[40] ^= 0x01;                      /* s tampered */
		CHECK(sfs_ed25519_verify(sha512_cb, NULL, k->pub, k->msg,
					 k->msg_len, tmp) == 0,
		      "RFC %s: flipped s byte accepted", k->name);

		if (k->msg_len) {                     /* message tampered */
			u8 *m = malloc(k->msg_len);

			memcpy(m, k->msg, k->msg_len);
			m[k->msg_len / 2] ^= 0x80;
			CHECK(sfs_ed25519_verify(sha512_cb, NULL, k->pub, m,
						 k->msg_len, k->sig) == 0,
			      "RFC %s: flipped msg byte accepted", k->name);
			free(m);
		}

		{                                     /* wrong pubkey */
			u8 pub2[32];

			memcpy(pub2, k->pub, 32);
			pub2[7] ^= 0x40;
			CHECK(sfs_ed25519_verify(sha512_cb, NULL, pub2, k->msg,
						 k->msg_len, k->sig) == 0,
			      "RFC %s: wrong pubkey accepted", k->name);
		}

		{                                     /* non-canonical s: s+L */
			unsigned int carry = 0;
			int j;

			memcpy(tmp, k->sig, 64);
			for (j = 0; j < 32; j++) {
				unsigned int v = tmp[32 + j] + ed_L[j] + carry;

				tmp[32 + j] = (u8)v;
				carry = v >> 8;
			}
			CHECK(sfs_ed25519_verify(sha512_cb, NULL, k->pub,
						 k->msg, k->msg_len, tmp) == 0,
			      "RFC %s: non-canonical s (s+L) accepted",
			      k->name);
		}
	}

	{                                             /* small-order pubkey */
		u8 pub[32] = { 0x01 };                /* identity: y = 1 */
		u8 sig[64] = { 0 };

		CHECK(sfs_ed25519_verify(sha512_cb, NULL, pub,
					 (const u8 *)"x", 1, sig) == 0,
		      "small-order (identity) pubkey accepted");
	}

	printf("rfc8032: %u vectors + negatives %s\n", (unsigned int)NKATS,
	       failures ? "FAILED" : "ok");
}

/* ── dalek cross-vectors (ed25519-vectors.txt from sfs-mkgolden) ─────────── */

static int unhex(const char *h, u8 *out, u32 n)
{
	u32 i;

	for (i = 0; i < n; i++) {
		unsigned int v;

		if (sscanf(h + 2 * i, "%2x", &v) != 1)
			return -1;
		out[i] = (u8)v;
	}
	return 0;
}

static void run_cross_vectors(const char *golden_dir)
{
	char path[4096];
	char line[8192];
	FILE *f;
	unsigned int nvec = 0;

	snprintf(path, sizeof(path), "%s/ed25519-vectors.txt", golden_dir);
	f = fopen(path, "r");
	if (!f) {
		failures++;
		printf("FAIL: cannot open %s (regenerate goldens)\n", path);
		return;
	}

	while (fgets(line, sizeof(line), f)) {
		char seed_h[65], pub_h[65], sig_h[129];
		char msg_h[4096];
		u8 seed[32], pub[32], sig[64], mysig[64];
		u8 msg[2048];
		u32 msg_len;
		struct sfs_ed25519_key key;

		if (line[0] == '#' || line[0] == '\n')
			continue;
		if (sscanf(line, "ED seed=%64s pub=%64s msg=%4095s sig=%128s",
			   seed_h, pub_h, msg_h, sig_h) != 4) {
			failures++;
			printf("FAIL: unparseable vector line: %s", line);
			continue;
		}
		if (unhex(seed_h, seed, 32) || unhex(pub_h, pub, 32) ||
		    unhex(sig_h, sig, 64)) {
			failures++;
			printf("FAIL: bad hex in vector line: %s", line);
			continue;
		}
		if (strcmp(msg_h, "-") == 0) {
			msg_len = 0;
		} else {
			msg_len = (u32)strlen(msg_h) / 2;
			if (msg_len > sizeof(msg) || unhex(msg_h, msg, msg_len)) {
				failures++;
				printf("FAIL: bad msg hex: %s", line);
				continue;
			}
		}

		CHECK(sfs_ed25519_expand(sha512_cb, NULL, seed, &key) == 0,
		      "cross %u: expand", nvec);
		CHECK(memcmp(key.pub, pub, 32) == 0,
		      "cross %u: pubkey != dalek pubkey", nvec);
		CHECK(sfs_ed25519_verify(sha512_cb, NULL, pub, msg, msg_len,
					 sig) == 1,
		      "cross %u: dalek signature rejected", nvec);
		CHECK(sfs_ed25519_sign(sha512_cb, NULL, &key, msg, msg_len,
				       mysig) == 0,
		      "cross %u: sign", nvec);
		CHECK(memcmp(mysig, sig, 64) == 0,
		      "cross %u: C signature != dalek signature", nvec);

		/* tamper: dalek sig over a flipped payload must fail here */
		if (msg_len) {
			msg[0] ^= 0xff;
			CHECK(sfs_ed25519_verify(sha512_cb, NULL, pub, msg,
						 msg_len, sig) == 0,
			      "cross %u: tampered payload accepted", nvec);
		}
		nvec++;
	}
	fclose(f);

	CHECK(nvec >= 16, "expected >= 16 dalek cross-vectors, got %u", nvec);
	printf("dalek cross-vectors: %u %s\n", nvec,
	       failures ? "FAILED" : "ok");
}

int main(int argc, char **argv)
{
	if (argc != 2) {
		fprintf(stderr, "usage: %s <goldendir>\n", argv[0]);
		return 2;
	}

	run_rfc_kats();
	run_cross_vectors(argv[1]);

	if (failures) {
		printf("sfs_edtest: %d FAILURE(S)\n", failures);
		return 1;
	}
	printf("sfs_edtest: ALL OK\n");
	return 0;
}
